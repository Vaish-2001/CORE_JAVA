class FormaL_Argument_Example
{
  public static void main(String[]args)
    {
     System.out.println("hi from main");
     myMethod("ramesh");
    }
  public static void myMethod(String name)
    {
       System.out.println("hello from myMethod()"+name);
    }
 }

//hi from main
//hello from myMethod()ramesh
